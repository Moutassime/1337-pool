#include <unistd.h>

void	ft_is_negative(int n)
	{
	char	var;

	if (n < 0 )
	{	
		var = 'N';
		write(1, &var, 1);
	}	
	else
	{	
		var = 'P';
		write(1, &var, 1);
	}
}
